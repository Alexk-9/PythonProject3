# 1. Написать программу, которая будет складывать, вычитать, умножать или делить два числа.
# Числа и знак операции вводятся пользователем. После выполнения вычисления программа
# не должна завершаться, а должна запрашивать новые данные для вычислений.
# Завершение программы должно выполняться при вводе символа '0' в качестве знака операции.
# Если пользователь вводит неверный знак (не '0', '+', '-', '*', '/'),
# то программа должна сообщать ему об ошибке и снова запрашивать знак операции.
# Также сообщать пользователю о невозможности деления на ноль, если он ввел 0 в качестве делителя.

def calculate(l, m, n):
    if n == '+':
        print(f'{l} + {m} = {l + m}')
    elif n == '-':
        print(f'{l} - {m} = {l - m}')
    elif n == '*':
        print(f'{l} * {m} = {l * m}')
    elif n == '/':
        if m != 0:
            print(f'{l} / {m} = {l / m}')
        else:
            print('На 0 делить нельзя')
    elif n == '0':
        exit(0)
    else:
        print('Нужно вводить знаки математических операций "+", "-", "*", "/" или "0" для выхода')


z = 1
while z != 0:
    x = int(input('Введите первое число: '))
    y = int(input('Введите второе число: '))
    z = input('Введите знак операции над числами: "+", "-", "*", "/" или "0" для выхода: ')
    calculate(x, y, z)


# 2. Посчитать четные и нечетные цифры введенного натурального числа.
# Например, если введено число 34560, то у него 3 четные цифры
# (4, 6 и 0) и 2 нечетные (3 и 5).

def ff(nn):
    i_o = 0
    i_n = 0
    for i in nn:
        if int(i) % 2 == 0:
            i_o += 1
        else:
            i_n += 1
    return print(f'в числе {nn} чётных цифр - {i_o}, нечётных - {i_n}')


num = input('Введите натуральное число: ')
ff(num)

