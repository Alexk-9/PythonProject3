# 9. Среди натуральных чисел, которые были введены,
# найти наибольшее по сумме цифр. Вывести на экран это число и сумму его цифр.

def f(x):
    return sum([int(j) for j in str(x)])


a = [int(x) for x in input('Введите числа через пробел ').split()]
b = []
m_x = 0

for x in a:
    fn = f(x)
    if fn > m_x:
        m_x = fn
        b = [fn]
print(f' наибольшее по сумме цифр число {x} сумма цифр ', *b)
